namespace ManagerPeople.Models.Enum;

/// <summary>
/// Enumeration of the eight major human blood types in the ABO and Rh classification systems.
/// </summary>
public enum EBloodType
{
    /// <summary>
    /// A Positive (A+)
    /// </summary>
    A_Positive,

    /// <summary>
    /// A Negative (A-)
    /// </summary>
    A_Negative,

    /// <summary>
    /// B Positive (B+)
    /// </summary>
    B_Positive,

    /// <summary>
    /// B Negative (B-)
    /// </summary>
    B_Negative,

    /// <summary>
    /// AB Positive (AB+)
    /// </summary>
    AB_Positive,

    /// <summary>
    /// AB Negative (AB-)
    /// </summary>
    AB_Negative,

    /// <summary>
    /// O Positive (O+)
    /// </summary>
    O_Positive,

    /// <summary>
    /// O Negative (O-)
    /// </summary>
    O_Negative
}
